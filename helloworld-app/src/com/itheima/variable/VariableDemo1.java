package com.iteima.variable;

public class VariableDemo1 {
    public static void main(String[] args) {
        // 学会使用变量。
        // 数据类型 变量名 = 初始值;
        double money = 9.5;
        System.out.println(money);

        // 收红包 （从=右向左看）
        money = money + 0.5;
        System.out.println(money);

        System.out.println("--------------");
        int age = 21;
        System.out.println(age);

        age = 25;
        System.out.println(age);



    }
}
